    scoutln(out, bytesOut, "<form action=\"ConsolidationSalesSalesPersonExecute\" name=go enctype=\"application/x-www-form-urlencoded\" method=POST>");
    scoutln(out, bytesOut, "<input type=hidden name=unm value=" + unm + ">");
